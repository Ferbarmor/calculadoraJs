function suma(a, b) {
    return a + b;
  }
  
  function resta(a, b) {
    return a - b;
  }
  
  function multiplicar(a,b){
    return a*b;
  }

  function dividir(a,b){
    return a/b;
  }

  function raiz(a){
    return Math.sqrt(a);
  }
  
  module.exports = { suma, resta, multiplicar,dividir,raiz };//Para pasar el analizador hola